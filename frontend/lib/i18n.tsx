const languageOptions = [
  { code: "en", label: "English" },
  { code: "es", label: "Español" },
];

type I18nContextValue = {
  language: string;
  setLanguage: (lang: string) => void;
  t: (key: string) => string;
  options: typeof languageOptions;
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const dispatch = useAppDispatch();
  const language = useAppSelector((state) => state.i18n.language);

  useEffect(() => {
    if (typeof window === "undefined") {
      return;
    }
    const stored = window.localStorage.getItem("ml_language");
    if (stored && dictionaries[stored]) {
      dispatch(setLanguageAction(stored));
      return;
    }
    const navigatorLang = window.navigator.language.split("-")[0];
    if (dictionaries[navigatorLang]) {
      dispatch(setLanguageAction(navigatorLang));
    }
  }, [dispatch]);

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem("ml_language", language);
    }
  }, [language]);

  const setLanguage = useCallback((lang: string) => {
    if (!dictionaries[lang]) {
      return;
    }
    dispatch(setLanguageAction(lang));
  }, [dispatch]);

  const t = useCallback(
    (key: string) => dictionaries[language]?.[key] || dictionaries.en[key] || key,
    [language]
  );

  const value = useMemo(
    () => ({ language, setLanguage, t, options: languageOptions }),
    [language, setLanguage, t]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}
